u8t GT32_WirteByte(u8t bytes)
{
	u8t i;
	//u8t temp;
	Clr_GTSCK;
//	for(temp=0;temp<20;temp++);            //不能太快
	for(i=0;i<8;i++)
	{

		if(0x80 == (bytes & 0x80))
		{
			Set_GTMOSI;
		}
		else
		{
			Clr_GTMOSI;
		}
		bytes = (bytes<<1);
		Set_GTSCK;
	//	for(temp=0;temp<20;temp++);			   //使用模式SPI时间的延时要把握好，不能太短	   

		if(GPIO_ReadInputDataBit(GPIOB, GPIO_Pin_14) !=0)
			bytes |=1;
		else
			bytes&=~1;

		Clr_GTSCK;
	//	for(temp=0;temp<20;temp++);            //不能太快

	}
	return (bytes);
}

void Char2Lattice(u16t ucode,u8t *latticeArray,u16t fontSize)
{
	u8t i;
	u8t latticeLength;
	u32t fontAddress;
	switch(fontSize)
	{
		case FONT_SIZE_11X12:
			latticeLength=FONT_11X12_LENGTH;
			fontAddress=GetFont11X12Address(ucode);
			break;
		case FONT_SIZE_15X16:
			latticeLength=FONT_15X16_LENGTH;
			fontAddress=GetFont15X16Address(ucode);		
			break;
		case FONT_SIZE_7X8_ASCII:
			latticeLength=FONT_7X8_LENGTH;
			fontAddress=GetFont7X8AsciiAddress(ucode);			
			break;
		case FONT_SIZE_8X16_ASCII:
			latticeLength=FONT_8X16_LENGTH;
			fontAddress=GetFont8X16AsciiAddress(ucode);			
			break;
		default:
			break;
	}
		
  	Clr_GTCE;                    
	GT32_WirteByte(0x03);     
  	GT32_WirteByte((u8t)(fontAddress>>16));           
	GT32_WirteByte((u8t)(fontAddress>>8));
	GT32_WirteByte((u8t)fontAddress);
	for(i=0;i<latticeLength;i++)
	{
		latticeArray[i]=GT32_WirteByte(0xff);
  	}
  	Set_GTCE; 	
}

u32t GetFont11X12Address(u16t ucode)
{
	u32t baseAdd=FONT_11X12_BASE;
	u32t address=0;
	u16t i=0;
	if(ucode>=0x3400&&ucode<=0x4DB5)
		address=(ucode-0x3400)*24+baseAdd;
	else if(ucode>=0x4E00&&ucode<=0x9FA5)
		address=(ucode-0x4E00+6582)*24+baseAdd;
	else if((ucode>=0xFF00&&ucode<=0xFF5E)||(ucode>=0x20&&ucode<=0x7e))
	{
		if(ucode==0xFF00||ucode==0x20)
			address=(27484+538)*24+baseAdd;
		else if(ucode>0xFF00&&ucode<=0xFF5E)
			address=(ucode-0xFF00+27484+987)*24+baseAdd;
		else if(ucode>0x20&&ucode<=0x7e)
			address=(ucode-0x20+27494+987)*24+baseAdd;
	}
	else if((ucode>=0x00A1&&ucode<=0x33D5)||(ucode>=0xE76C&&ucode<=0xFFE5))
	{
		for(i=0;i<1088;i++)
		{
			if(ucode==ZFTABLE[i])
				break;
		}
		address=i*24+27484*24+baseAdd;
	}
	return address;
}

